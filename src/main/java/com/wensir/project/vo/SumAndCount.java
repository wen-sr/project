package com.wensir.project.vo;

public class SumAndCount {
    Integer sumQty;
    Integer countQty;
    Integer completeQty;
    Integer completeCount;

    public Integer getCompleteQty() {
        return completeQty;
    }

    public void setCompleteQty(Integer completeQty) {
        this.completeQty = completeQty;
    }

    public Integer getCompleteCount() {
        return completeCount;
    }

    public void setCompleteCount(Integer completeCount) {
        this.completeCount = completeCount;
    }

    public Integer getSumQty() {
        return sumQty;
    }

    public void setSumQty(Integer sumQty) {
        this.sumQty = sumQty;
    }

    public Integer getCountQty() {
        return countQty;
    }

    public void setCountQty(Integer countQty) {
        this.countQty = countQty;
    }
}
